		<h2><span>THE ANNUAL ORGANIZER</span></h1>
		
		<div class="description">
<span>Have you ever had that moment in which you suddenly thought 'Wow, that week went by so fast!' or 'I can't believe it's already September!'?
<br/>There is often so much going on in our lives we struggle to keep track. We do our best, keeping daily diaries or using weekly organizers, but it's hard to look ahead more than a week or so and we always end up losing track of the grand scheme of things. 
<br/>At the same time, we seem to be constantly working towards the future. We set up our daily lifes to work towards building a better future for ourselves and others. While this is a noble thing, it has the unfortunate side-effect of losing track of the moment. It becomes easy to forget life as it happens around you. 
<br/>Both of these things can be very hard. What's even harder is having to do both at once, on a daily basis. It's difficult to both 'prepare for the future' and 'seize the day'. What we need is a way to keep track of things that are far-off but also remind us of the time given to us each day. We need a different scale to view time with.
<br/>We need something to help us slow down and take a step back. 
<br/>Enter YearCircler. It's an annual clock, meaning it takes an entire year to complete just one full rotation. It tries to provide a way to keep track of the 'now' while seasons pass. Every day, you'll see 'today' move just a little bit, reminding you of the ever-moving present. However, it also allows you to set markers for important events in the future so you don't lose track of what's important in the long-term. These can be all kinds of things: future goals you set for yourself, important deadlines for a project you're working on (or planning to work on in the future!) or simple things like your friends' birthdays (and your own, of course!). In short: YearCircler helps you look at the future while keeping you in the present.
